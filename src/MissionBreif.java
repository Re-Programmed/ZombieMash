
public class MissionBreif {
	public final String difficulty;
	public final String deathLim;
	public final String duration;
	public final String command;
	public final int mis;
	
	public MissionBreif(String difficulty, String deathLim, String duration, String command, int mis)
	{
		this.difficulty = difficulty;
		this.deathLim = deathLim;
		this.duration = duration;
		this.command = command;
		this.mis = mis;
	}
}
